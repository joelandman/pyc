# Test arithmetic operations
def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def main():
    x = add(10, 5)
    print(x)
    y = subtract(20, 8)
    print(y)
    z = multiply(6, 7)
    print(z)
    w = divide(15, 3)
    print(w)

if __name__ == "__main__":
    main()
